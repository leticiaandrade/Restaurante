package classes;

public class Conta {
	int codigoConta;
	String nomeCliente;
	
	

	public String getNomeCliente() {
		return nomeCliente;
	}

	public void setNomeCliente(String nomeCliente) {
		this.nomeCliente = nomeCliente;
	}

	public int getCodigoConta() {
		return codigoConta;
	}

	public void setCodigoConta(int codigo) {
		this.codigoConta = codigo;
	}
	
	

}
